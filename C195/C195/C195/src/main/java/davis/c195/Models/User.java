package davis.c195.Models;


/**
 * Model for Users
 * @author Brandon Davis
 */
public class User {
    private String Username;

    /**
     * Constructor for Username
     */
    public User(){
        this.Username=Username;

    }

    /**
     * Getter for Username
     * @return
     */
    public String getUsername() {
        return Username;
    }

    /**
     * Setter for Username
     * @param username
     */
    public void setUsername(String username) {
        Username = username;
    }
}
